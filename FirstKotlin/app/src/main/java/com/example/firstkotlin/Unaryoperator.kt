package com.example.firstkotlin

fun main(args: Array<String>) {

    var num:Double = 7.6
    var ischeck:Boolean = true

    println("+number =" + +num)
    println("-number =" + -num)
    println("++number =" + ++num)
    println("--number =" + --num)
    println("!isCheck =" + !ischeck)

    var res:Double = 4.7
    println("res++ = " + res++)
    println("res = " + res)
    println("++res = " + ++res)
    println("res = " + res)



}